firstNumber = float(input('Digite o primeiro número: '))
secondNumber = float(input('Digite o segundo número: '))
thirdNumber = float(input('Digite o terceiro número: '))

if ((firstNumber + secondNumber) < thirdNumber):
  print("A adição do primeiro e segundo número é menor que o terceiro número")
else:
  print("A adição do primeiro e segundo número é maior que o terceiro número")

